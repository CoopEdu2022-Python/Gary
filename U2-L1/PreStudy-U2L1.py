# 1. 什么是面向过程？什么是面向对象？（多行注释）
'''
面向过程
把完成某一个需求的所有步奏从头到尾逐步实现
根据开发需求，将某些功能独立的代码封装乘一个又一个函数
最后完成的代码，就是顺序地调用不同的函数
特点注重步奏与过程，不注重职责分工
开发复杂相符，没有固定的套路，开发难度大
面型对象
相比叫函数，面向对象时更大的封装，根据职责在一个对象中封装多个方法
在完成某一个需求钱，首先确定职责
根据职责确定不同的对象内部风中不同方法
注重对象和职责，不同的对象承担不同的职责
更加适合相对复杂的需求变化，时专门因对复杂项目开发，提供固定套路

'''

